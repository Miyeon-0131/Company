export function getIdleChatter(employeeId: string, tick: number): string {
  const roleLines = ROLE_CHATTER[employeeId] ?? [];
  const pool = [...GLOBAL_CHATTER, ...roleLines];
  const seed =
    employeeId.split("").reduce((n, c) => n + c.charCodeAt(0), 0) + tick;
  return pool[seed % pool.length]!;
}
