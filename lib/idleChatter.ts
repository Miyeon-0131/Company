const FALLBACK = ["☕ 待机中，等待新任务…"];

/** 取某岗位的第 n 条职务相关台词 */
export function getRoleChatter(employeeId: string, index: number): string {
  const lines = ROLE_CHATTER[employeeId] ?? FALLBACK;
  return lines[index % lines.length]!;
}

